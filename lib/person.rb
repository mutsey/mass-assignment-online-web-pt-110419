class Person
attr_accessor:
jane_attributes = {name: "Jane", hair_color: "Brown", :eye_color: "Green"}
 
Jane = Person.new(Jane_attributes)
Jane.name       # => "Jane"
Jane.hair_color  #=> "Brown"
Jane.eye_color #=> "Green"

attr_accessor: 
Carrie-attributes: = {name: "Carrie" , height: "5'5" , :handed "Left"} 

Carrie= Person.new(Carrie_attributes)
Carrie.name #=> "Carrie"
Carrie.height #=> "5'5"
Carrie.handed #=> "Left"

end